x2= 5*(t2^2) + 20*t2 + 5;
vr= (x2-x1)/(t2-t1);
 
% kecepatan sesaat t1
v1= 10*t1 + 20;
 
% kecepatan sesaat t2
v2= 10*t2 + 20;
 
% Percepatan rata-rata
ar= (v2-v1)/(t2-t1);
 
% Percepatan sesaat
as= 10;
 
% Output
set(handles.edit3,'string', vr);
set(handles.edit4,'string', v1);
set(handles.edit5,'string', v2);
set(handles.edit6,'string', ar);
set(handles.edit7,'string', as);


